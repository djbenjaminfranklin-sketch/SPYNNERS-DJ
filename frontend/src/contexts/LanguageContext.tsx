interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
  getCurrentFlag: () => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>('en'); // Default to English
  const [, forceUpdate] = useState(0); // Force re-render trigger

  useEffect(() => {
    loadLanguage();
  }, []);

  const loadLanguage = async () => {
    try {
      const savedLang = await AsyncStorage.getItem('app_language');
      console.log('[Language] Loaded language from storage:', savedLang);
      if (savedLang && LANGUAGES.some(l => l.code === savedLang)) {
        setLanguageState(savedLang as Language);
      }
    } catch (error) {
      console.error('Error loading language:', error);
    }
  };

  const setLanguage = async (lang: Language) => {
    console.log('[Language] Setting language to:', lang);
    try {
      await AsyncStorage.setItem('app_language', lang);
      setLanguageState(lang);
      // Force re-render of all children
      forceUpdate(n => n + 1);
    } catch (error) {
      console.error('Error saving language:', error);
    }
  };

  // Create t function that always uses current language
  const t = (key: string): string => {
    const result = translations[language]?.[key] || translations['en']?.[key] || key;
    return result;
  };

  const getCurrentFlag = (): string => {
    return LANGUAGES.find(l => l.code === language)?.flag || 'ğŸ‡¬ğŸ‡§';
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, getCurrentFlag }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
